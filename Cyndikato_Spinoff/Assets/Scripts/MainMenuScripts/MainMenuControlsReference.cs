// MAIN MENU NAVIGATION CONTROLS REFERENCE
// =====================================

/* KEYBOARD CONTROLS (Player 1):
 * W / Up Arrow    = Navigate Up
 * S / Down Arrow  = Navigate Down  
 * A               = Navigate Left
 * D               = Navigate Right
 * Enter / Space   = Select current menu item
 */

/* PS4 CONTROLLER CONTROLS (Player 1):
 * D-Pad Up        = Navigate Up
 * D-Pad Down      = Navigate Down
 * D-Pad Left      = Navigate Left (Left Arrow)
 * D-Pad Right     = Navigate Right (Right Arrow)
 * Left Stick      = Alternative navigation (analog)
 * X Button (Cross)= Select current menu item (South Button)
 * Circle Button   = Alternative select (some regions)
 * Start Button    = Quick start game (bypass menu)
 */

/* MENU BEHAVIOR:
 * - Only Player 1 can control the main menu
 * - Navigation wraps around (Start -> Settings -> Exit -> Start)
 * - Both horizontal and vertical navigation work the same way
 * - Visual feedback shows current selection
 * - Audio feedback on navigation and selection (if configured)
 */

/* MENU OPTIONS:
 * 1. Start Game   -> Loads Character Select scene
 * 2. Settings     -> Loads Settings scene (when implemented)
 * 3. Exit Game    -> Quits application
 */